    public function index()
    {
      $usuario = Auth::user();

      $niveles = Nivel::where('id','>', 0)->with(['grados' => function($query) use ($usuario){
        $query->where('sede_id', '=', $usuario->empleado->sede_id);
      }])->get();

        return view('platform.configuracion_general')->with([
          'cursos_lista'  =>  false,
          'niveles'       =>  $niveles,
          'grado_'         =>  null
        ]);
    }

